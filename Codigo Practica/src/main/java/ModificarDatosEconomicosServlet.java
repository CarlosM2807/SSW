    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        processRequest(request, response);
    }

    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        String capital = request.getParameter("capital_inicial");
        String ingreso = request.getParameter("ingreso_mensual");
        String gasto = request.getParameter("gasto_mensual");
        
        User usuario = new User();
        usuario.setCapital(capital);
        usuario.setIngreso(ingreso);
        usuario.setGasto(gasto);
        
        try {
            HttpSession session = request.getSession();
            String id = (String) session.getAttribute("id");
            UserDB.updateDatosEconomicos(usuario, id);
        } catch (Exception ex) {
            Logger.getLogger(ModificarUsuarioServlet.class.getName()).log(Level.SEVERE, null, ex);
        }
        RequestDispatcher view = request.getRequestDispatcher("/resumen.jsp");
        view.forward(request, response);
    }

